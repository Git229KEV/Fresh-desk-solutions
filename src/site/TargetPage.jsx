const TargetPage = ({ title, image, content, highlights }) => {

    // Hide scrollbar when this component is mounted
    React.useEffect(() => {
        // Create style element to hide scrollbar
        const style = document.createElement('style');
        style.innerHTML = `
            body::-webkit-scrollbar { display: none; }
            body { -ms-overflow-style: none; scrollbar-width: none; }
        `;
        document.head.appendChild(style);

        // Cleanup on unmount
        return () => {
            document.head.removeChild(style);
        };
    }, []);

    // Get icon based on the string key provided in highlights
    const getIcon = (iconName) => {
        if (!iconName) return <Icons.Default />;

        const key = Object.keys(Icons).find(k => k.toLowerCase() === iconName.toLowerCase());
        const IconComponent = Icons[key] || Icons.Default;
        return <IconComponent />;
    };

    return (
        <div className="target-page">
            {/* Hero Section - Same as SolutionPage for consistency */}
            <div className="target-hero">
                <img src={image} alt={title} />
                <div className="hero-overlay">
                    <h1 className="hero-title">{title}</h1>
                </div>
            </div>

            {/* Content & Columns */}
            <div className="target-content-wrapper">

                {/* Intro Text */}
                <div className="target-intro">
                    <p>{content}</p>
                </div>

                {/* Columns Container */}
                {highlights && highlights.length > 0 && (
                    <div className="target-columns-container">
                        {highlights.map((item, index) => (
                            <div key={index} className="target-column">
                                <div className="column-header">
                                    <div className="column-icon">
                                        {getIcon(item.icon)}
                                    </div>
                                    <h3 className="column-title">{item.title}</h3>
                                </div>

                                <div className="column-divider"></div>

                                <div className="column-content">
                                    {/* Splitting description by sentences to simulate a list if it's long, or just showing it */}
                                    <div className="column-description">
                                        <ul className="feature-list">
                                            {/* Simulate list items from the description for the 'check' look */}
                                            <li className="feature-item">
                                                <span className="feature-icon"><Icons.Check /></span>
                                                <span>{item.description}</span>
                                            </li>
                                        </ul>
                                    </div>
                                </div>


                            </div>
                        ))}
                    </div>
                )}

            </div>
        </div>
    );
};

export default TargetPage;
